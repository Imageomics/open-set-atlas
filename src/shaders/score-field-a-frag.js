export const scoreFieldAFrag = [
  "uniform sampler2D scoreMap;",
  "uniform sampler2D classIdxMap;",
  "uniform float threshold;",
  "uniform vec3 classColors[5];",
  "uniform float isGradient;",
  "uniform float maxTrainScore;",
  "uniform float invertThreshold;",
  "varying vec2 vUv;",
  "void main() {",
  "  float score = texture2D(scoreMap, vUv).r;",
  "  float cidxRaw = texture2D(classIdxMap, vUv).r;",
  "  int ci = int(cidxRaw * 255.0 + 0.5);",
  "  bool pass = (invertThreshold > 0.5) ? (score <= threshold) : (score >= threshold);",
  "  if (pass) {",
  "    vec3 color = classColors[0];",
  "    if (ci == 1) color = classColors[1];",
  "    else if (ci == 2) color = classColors[2];",
  "    else if (ci == 3) color = classColors[3];",
  "    else if (ci == 4) color = classColors[4];",
  "    float t = 0.0;",
  "    if (invertThreshold > 0.5) {",
  "      float denom = threshold - maxTrainScore;",
  "      t = (abs(denom) > 0.0001) ? (threshold - score) / denom : 1.0;",
  "    } else {",
  "      float denom = maxTrainScore - threshold;",
  "      t = (abs(denom) > 0.0001) ? (score - threshold) / denom : 1.0;",
  "    }",
  "    float alpha = 0.01 + 0.74 * clamp(t, 0.0, 1.0);",
  "    gl_FragColor = vec4(color, alpha);",
  "  } else {",
  "    gl_FragColor = vec4(0.0, 0.0, 0.0, 0.0);",
  "  }",
  "}"
].join("\n");
