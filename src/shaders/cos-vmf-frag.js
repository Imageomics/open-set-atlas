import { glslDirFromUV, glslPickColor } from './preamble.js';

export const cosVmfFrag = [
  "precision highp float;",
  "uniform vec3 classDir[5];",
  "uniform float classKappa[5];",
  "uniform float perClassThr[5];",
  "uniform vec3 classColors[5];",
  "uniform float isGradient;",
  "uniform float maxTrainScores[5];",
  "varying vec2 vUv;",
  glslDirFromUV,
  "void main() {",
  "  vec3 x = dirFromUV(vUv);",
  "  float s0 = classKappa[0] * dot(classDir[0], x);",
  "  float s1 = classKappa[1] * dot(classDir[1], x);",
  "  float s2 = classKappa[2] * dot(classDir[2], x);",
  "  float s3 = classKappa[3] * dot(classDir[3], x);",
  "  float s4 = classKappa[4] * dot(classDir[4], x);",
  "  int bestCI = -1;",
  "  float bestRatio = -100000.0;",
  "  float bestScore = 0.0;",
  "  if (s0 >= perClassThr[0]) { float r = s0 / max(perClassThr[0], 0.0000001); if (r > bestRatio) { bestCI = 0; bestRatio = r; bestScore = s0; } }",
  "  if (s1 >= perClassThr[1]) { float r = s1 / max(perClassThr[1], 0.0000001); if (r > bestRatio) { bestCI = 1; bestRatio = r; bestScore = s1; } }",
  "  if (s2 >= perClassThr[2]) { float r = s2 / max(perClassThr[2], 0.0000001); if (r > bestRatio) { bestCI = 2; bestRatio = r; bestScore = s2; } }",
  "  if (s3 >= perClassThr[3]) { float r = s3 / max(perClassThr[3], 0.0000001); if (r > bestRatio) { bestCI = 3; bestRatio = r; bestScore = s3; } }",
  "  if (s4 >= perClassThr[4]) { float r = s4 / max(perClassThr[4], 0.0000001); if (r > bestRatio) { bestCI = 4; bestRatio = r; bestScore = s4; } }",
  "  if (bestCI >= 0) {",
  glslPickColor,
  "    float alpha = 0.55;",
  "    if (isGradient > 0.5) {",
  "      float thr = perClassThr[0]; float mts = maxTrainScores[0];",
  "      if (bestCI == 1) { thr = perClassThr[1]; mts = maxTrainScores[1]; }",
  "      else if (bestCI == 2) { thr = perClassThr[2]; mts = maxTrainScores[2]; }",
  "      else if (bestCI == 3) { thr = perClassThr[3]; mts = maxTrainScores[3]; }",
  "      else if (bestCI == 4) { thr = perClassThr[4]; mts = maxTrainScores[4]; }",
  "      float t = (mts > thr + 0.0001) ? (bestScore - thr) / (mts - thr) : 1.0;",
  "      alpha = 0.01 + 0.74 * clamp(t, 0.0, 1.0);",
  "    }",
  "    gl_FragColor = vec4(color, alpha);",
  "  } else {",
  "    gl_FragColor = vec4(0.0);",
  "  }",
  "}"
].join("\n");
