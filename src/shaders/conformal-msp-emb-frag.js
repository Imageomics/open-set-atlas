import { glslDirFromUV, glslPickColor } from './preamble.js';

export const conformalMspEmbFrag = [
  "precision highp float;",
  "uniform vec3 classW[5];",
  "uniform float classB[5];",
  "uniform vec3 classMuHat[5];",
  "uniform float threshold;",
  "uniform vec3 classColors[5];",
  "uniform int scoreMode;",
  "varying vec2 vUv;",
  glslDirFromUV,
  "void main() {",
  "  vec3 x = dirFromUV(vUv);",
  "  float sc0, sc1, sc2, sc3, sc4;",
  "  if (scoreMode == 1) {",
  "    sc0 = dot(x, classMuHat[0]);",
  "    sc1 = dot(x, classMuHat[1]);",
  "    sc2 = dot(x, classMuHat[2]);",
  "    sc3 = dot(x, classMuHat[3]);",
  "    sc4 = dot(x, classMuHat[4]);",
  "  } else {",
  "    float l0 = dot(classW[0], x) + classB[0];",
  "    float l1 = dot(classW[1], x) + classB[1];",
  "    float l2 = dot(classW[2], x) + classB[2];",
  "    float l3 = dot(classW[3], x) + classB[3];",
  "    float l4 = dot(classW[4], x) + classB[4];",
  "    float maxL = max(max(max(max(l0,l1),l2),l3),l4);",
  "    float e0 = exp(l0 - maxL); float e1 = exp(l1 - maxL);",
  "    float e2 = exp(l2 - maxL); float e3 = exp(l3 - maxL); float e4 = exp(l4 - maxL);",
  "    float sumE = e0 + e1 + e2 + e3 + e4;",
  "    sc0 = e0/sumE; sc1 = e1/sumE; sc2 = e2/sumE; sc3 = e3/sumE; sc4 = e4/sumE;",
  "  }",
  "  int bestCI = -1; float bestScore = -1.0; int count = 0;",
  "  if (sc0 >= threshold) { count++; if (sc0 > bestScore) { bestCI = 0; bestScore = sc0; } }",
  "  if (sc1 >= threshold) { count++; if (sc1 > bestScore) { bestCI = 1; bestScore = sc1; } }",
  "  if (sc2 >= threshold) { count++; if (sc2 > bestScore) { bestCI = 2; bestScore = sc2; } }",
  "  if (sc3 >= threshold) { count++; if (sc3 > bestScore) { bestCI = 3; bestScore = sc3; } }",
  "  if (sc4 >= threshold) { count++; if (sc4 > bestScore) { bestCI = 4; bestScore = sc4; } }",
  "  if (count == 0) { gl_FragColor = vec4(0.0); return; }",
  glslPickColor,
  "  float alpha = (count == 1) ? 0.55 : 0.20;",
  "  gl_FragColor = vec4(color, alpha);",
  "}"
].join("\n");
