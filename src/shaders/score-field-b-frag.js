export const scoreFieldBFrag = [
  "uniform sampler2D scores04;",
  "uniform sampler2D score4;",
  "uniform float perClassThr[5];",
  "uniform vec3 classColors[5];",
  "uniform float isGradient;",
  "uniform float maxTrainScores[5];",
  "varying vec2 vUv;",
  "void main() {",
  "  vec4 s04 = texture2D(scores04, vUv);",
  "  float s4val = texture2D(score4, vUv).r;",
  "  float scores[5];",
  "  scores[0] = s04.r; scores[1] = s04.g; scores[2] = s04.b; scores[3] = s04.a; scores[4] = s4val;",
  "  int bestCI = -1;",
  "  float bestRatio = -100000.0;",
  "  float bestScore = 0.0;",
  "  for (int ci = 0; ci < 5; ci++) {",
  "    if (scores[ci] >= perClassThr[ci]) {",
  "      float ratio = scores[ci] / max(perClassThr[ci], 0.0000001);",
  "      if (ratio > bestRatio) {",
  "        bestCI = ci;",
  "        bestRatio = ratio;",
  "        bestScore = scores[ci];",
  "      }",
  "    }",
  "  }",
  "  if (bestCI >= 0) {",
  "    vec3 color = classColors[0];",
  "    if (bestCI == 1) color = classColors[1];",
  "    else if (bestCI == 2) color = classColors[2];",
  "    else if (bestCI == 3) color = classColors[3];",
  "    else if (bestCI == 4) color = classColors[4];",
  "    float alpha = 0.55;",
  "    if (isGradient > 0.5) {",
  "      float thr = perClassThr[0];",
  "      float mts = maxTrainScores[0];",
  "      if (bestCI == 1) { thr = perClassThr[1]; mts = maxTrainScores[1]; }",
  "      else if (bestCI == 2) { thr = perClassThr[2]; mts = maxTrainScores[2]; }",
  "      else if (bestCI == 3) { thr = perClassThr[3]; mts = maxTrainScores[3]; }",
  "      else if (bestCI == 4) { thr = perClassThr[4]; mts = maxTrainScores[4]; }",
  "      float t = (mts > thr + 0.0001) ? (bestScore - thr) / (mts - thr) : 1.0;",
  "      alpha = 0.01 + 0.74 * clamp(t, 0.0, 1.0);",
  "    }",
  "    gl_FragColor = vec4(color, alpha);",
  "  } else {",
  "    gl_FragColor = vec4(0.0);",
  "  }",
  "}"
].join("\n");
